#include "libft.a"

/* ************************************************************************** */
/* ft_free_2d_array(char ***cmd) frees a two dimentional array				  */
/* ************************************************************************** */

void ft_free_2d_array(char ***cmd)
{
    int index = 0;

    while ((*cmd)[index])
    {
        free((*cmd)[index]);
        index++;
    }
    free(*cmd);
    *cmd = NULL;
}